    <Pre>{`Father Blaire's Resurrection Mechanic:
Father Blaire dies, is buried for three days, and rises again. He must be buried in consecrated ground for the resurrection to occur.

The Draconians:
An alien race that abducted human children — including potential Christ vessels — as part of a larger scheme to halt or delay the second coming. They are not the only alien species on Earth; the Church has access to intergalactic technology through alliances with other species.

Cosmic Function of the Christ Child:
The Christ Child's function is to reset the Piscean age. Worlds that pass judgement enter the Aquarian age. Worlds that fail must restart the Piscean cycle. The Christ child accomplishes the reset by giving humans all knowledge — which, paradoxically, destroys the world by making it too powerful too soon. Michael's solution is to distribute the power as knowledge to all living things, ushering in the true Age of Aquarius without destruction.

The 47 Colonies:
Referenced but not yet fully explained — suggests a broader intergalactic civilization connected to Earth through the Church and alien alliances.`}</Pre>

